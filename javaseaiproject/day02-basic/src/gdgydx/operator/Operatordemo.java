package gdgydx.operator;

public class Operatordemo
{
    public static void main(String[] args)
    {
        printoperator();
    }
    //认识基本的算数运算符
    public static void printoperator()
        {
            int a = 10;
            int b = 5;
            System.out.println(a+b);
            System.out.println(a-b);
            System.out.println(a*b);
            System.out.println(a/b);
            System.out.println(a%b);
        }


}
