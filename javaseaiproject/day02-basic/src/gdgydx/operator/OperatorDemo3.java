package gdgydx.operator;

public class OperatorDemo3
{
    public static void main(String[] args)
    {
        print(10,10);
    }
    //理解关系运算符
    public static void print(int a , int b )
    {
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}
